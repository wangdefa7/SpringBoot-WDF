class Solution {
    public int lengthOfLongestSubstring(String s) {
    	String[] array = s.split("");
    	int len = array.length;
    	int max = 0,min = 0;
    	for (int i=0;i<len;i++){
    		for (int j = i;j<len;j++){
    			for (int h =j+1;h<len;h++){
					if (array[j] == array[h]){
						min = h-j;
					}
				}
    			if (max < min) max = min;
    			min = 0;
			}
		}
		System.out.println(max);
		return max;
    }
}
